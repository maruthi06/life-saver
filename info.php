<h1 style="font-size:28; color:brown">INSTRUCTION</h1>
<pre>
<p>
Blood donation involves collecting blood from a donor so it can be used to treat someone else.
Blood donations are an essential part of our healthcare system. If we did not have volunteers 
giving blood,many medical procedures we take for granted could not take place.
Doctors and surgeons rely on blood donations to carry out life-saving and life-enhancing 
treatments every day.

</p>
<h2 style="font-size:20; color:green">
How can I donate blood?
</h2>
<p>
1. Thousands of blood donation sessions are held each year by NHS Blood and Transplant, so it's 
   usually possible to attend one that is convenient for you.
2. You will need to answer some questions about your health and have a quick blood test before you
   can donate blood. This is to ensure there is no danger to yourself or someone else.
3. During a blood donation, a needle is used to collect 470ml (just under one pint) of your blood.
4. You will need to rest for a short while after a donation, and refreshments will be offered to 
   stop you feeling faint or dizzy.
5. It is usually recommended that men allow 12 weeks and women 16 weeks between donations.
</p>
<h3 style="font-size:20; color:red">
Before Your Donation
</h3>
<p>
Blood Donation Tip - Eat Healthy Maintain a healthy iron level in your diet by eating iron rich foods,
such as red meat, fish, poultry, beans, spinach, iron-fortified cereals and raisins.
Get a good night's sleep.
Drink an extra 16 oz. of water or nonalcoholic fluids before the donation.
Eat a healthy meal before your donation. Avoid fatty foods, such as hamburgers, fries or ice cream before
donating. (Fatty foods can affect the tests we do on your blood. If there is too much fat in your blood,
your donation cannot be tested for infectious diseases and the blood will not be used for transfusion.)
If you are a platelet donor, remember that your system must be free of aspirin for two days prior to
donation.Remember to bring your donor card, driver's license or two other forms of ID.
</p>
<h4 style="font-size:20; color:darkblue">
During Your Donation
</h4>
<p>
1.Wear clothing with sleeves that can be raised above the elbow.
Let the person taking your blood know if you have a preferred arm and show them any good veins that 
have been used successfully in the past to draw blood.
2.Relax, listen to music, talk to other donors or read during the donation process.
3.Take the time to enjoy a snack and a drink in the refreshments area immediately after donating.
</p>
